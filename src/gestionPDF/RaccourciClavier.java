/**
 * Classe qui gère l'attribution des touches pour chacune des fonctions
 */
public class RaccourciClavier {

        /**
         * touche variable affectée à la tache :
         * Transmettre le focus à la fenetre
         */
        private String toucheTransmetFocusFenetre;
        /**
         * touche affectée à la tâche :
         * affiche la page entrée s'il s'agit d'un entier compris
         * entre 1 et le nombre de page
         */
        private String toucheAffichePageEntree;


}
